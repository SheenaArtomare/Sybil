//=============================================================================
// 指定範囲内のランダムな値を取得する関数
// ---------------------------------------------------------------------------
//【引数】
// 最小値	min
// 最大値	max
//=============================================================================
int RandomRange(int min, int max)
{
	return rand() % (max - min + 1) + min;
}


//=============================================================================
// 値を最小・最大に収まるようにする関数
// ---------------------------------------------------------------------------
//【引数】
// 値		num
// 最小値	min
// 最大値	max
//=============================================================================
float Clamp(float num, float min, float max)
{
	if (num < min)
	{
		num = min;
	}
	else if (num > max)
	{
		num = max;
	}

	return num;
}


//=============================================================================
// 線形補間関数
// ---------------------------------------------------------------------------
//【引数】
// 始値		start
// 終値		end
// 変化率	t
//=============================================================================
float Lerp(float start, float end, float t)
{
	return start + (end - start) * t;
}


//=============================================================================
// イージング関数１（途中までほぼ０、そこから一気に１へ）
// ---------------------------------------------------------------------------
//【引数】
// 変化率（０〜１）	a
//=============================================================================
float EaseInQuint(float a)
{
	return a * a * a * a * a;
}


//=============================================================================
// 符号を取得する関数
//=============================================================================
int GetSign(int num)
{
	return num >= 0 ? 1 : -1;
}




//=============================================================================
// float型で負数にも対応しているランダムの値を返す関数
// digits:小数点以下の桁数(0.01f → 2), max:欲しい最大値, min:欲しい最小値
//=============================================================================
float RandomFloat(int digits, float max, float min)
{
	// 小数点を消すための変数を作成
	int g_digits = 1;
	for (int i = 0; i < digits; i++)
	{
		g_digits *= 10;
	}

	// 剰余算するための値を作成
	float surplus = (max - min) * g_digits;

	int random = rand() % (int)surplus;

	float ans;

	// 整数のランダムの値をfloat型へキャストと負数の処理
	ans = (float)random / g_digits + min;

	return ans;
}